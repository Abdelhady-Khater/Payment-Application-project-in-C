#ifndef STD_types
#define STD_types

typedef  char                   uint8_t;
typedef signed char             int8_t;

typedef unsigned short int      uint16_t;
typedef signed short int        int16_t;

typedef unsigned int            uint32_t;
typedef signed int              int32_t;

typedef float                   f32_t;
typedef double                  f64_t;

#endif